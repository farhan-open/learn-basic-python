# Operator = (Assignment)
x = 5
y = x + 2

# Operator += (Penugasan Penjumlahan)
x = 5
x += 3  # x = x + 3
print(x)  # Output: 8

# Operator -= (Penugasan Pengurangan)
x = 5
x -= 2  # x = x - 2
print(x)  # Output: 3

# Operator *= (Penugasan Perkalian)
x = 5
x *= 2  # x = x * 2
print(x)  # Output: 10

# Operator /= (Penugasan Pembagian)
x = 10
x /= 2  # x = x / 2
print(x)  # Output: 5.0

# Operator //= (Penugasan Pembagian Bulat)
x = 10
x //= 3  # x = x // 3
print(x)  # Output: 3

# Operator %= (Penugasan Sisa Pembagian)
x = 10
x %= 3  # x = x % 3
print(x)  # Output: 1

# Operator **= (Penugasan Pangkat)
x = 2
x **= 3  # x = x ** 3
print(x)  # Output: 8

# Operator &= (Penugasan AND bitwise)
x = 5   # Representasi biner: 0101
x &= 3  # x = x & 3 (0101 & 0011 = 0001)
print(x)  # Output: 1

# Operator |= (Penugasan OR bitwise)
x = 5   # Representasi biner: 0101
x |= 3  # x = x | 3 (0101 | 0011 = 0111)
print(x)  # Output: 7

# Operator ^= (Penugasan XOR bitwise)
x = 5   # Representasi biner: 0101
x ^= 3  # x = x ^ 3 (0101 ^ 0011 = 0110)
print(x)  # Output: 6

# Operator <<= (Penugasan left shift bitwise)
x = 5   # Representasi biner: 0101
x <<= 2  # x = x << 2 (0101 << 2 = 010100)
print(x)  # Output: 20

# Operator >>= (Penugasan right shift bitwise)
x = 5   # Representasi biner: 0101
x >>= 2  # x = x >> 2 (0101 >> 2 = 0001)
print(x)  # Output: 1

