#Variabel adalah tempat menyimpan data

a = 10
x = 5
panjang = 1000

# pemanggilan pertama
print("Nilai a = ", a)
print("Nilai x = ", x)
print("Nilai panjang = ", panjang)

# penamaan variabel yang dianjurkan
nilai_y = 15
juta10 = 10000000
nialiZ = 17.5

# pemanggilan kedua
a = 20 # nilai a = 10 sebelunya ditimpa dengan nilai baru yang kita pasang
print("Nilai a yang baru = ", a)

# assignment indirect
b = a # nilai a terbaru akan kita simpan juga ke dalam variabel b
print("Nilai b = ", b)